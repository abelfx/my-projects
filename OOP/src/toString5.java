/* toString() method: this method is a special object that all objects inherit,
 * it returns a string that textually represent an object either implicitly or explicitly
 */

public class toString5 {
	
	String model = "Cheve";
	String make = "Corvette";
	int price = 34567;
	double year = 2020;

    public String toString() {
    	String mystring = model + "\n" + make + "\n" + price +"\n" + year;
    	return mystring;
    	
    }
	
}
	
	

